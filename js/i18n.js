// Current language
let currentLang = localStorage.getItem('lhh-lang') || 'de';

/**
 * Get translation for a key path (e.g., "nav.projects")
 */
function t(keyPath, lang = currentLang) {
    const keys = keyPath.split('.');
    let value = translations[lang];

    for (const key of keys) {
        if (value && typeof value === 'object') {
            value = value[key];
        } else {
            return keyPath; // Return key if not found
        }
    }

    return value || keyPath;
}

/**
 * Update all elements with data-i18n attribute
 */
function updatePageTranslations() {
    // Update text content
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        el.textContent = t(key);
    });

    // Update placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        el.placeholder = t(key);
    });

    // Update HTML lang attribute
    document.documentElement.lang = currentLang;
}

/**
 * Set language and update UI
 */
function setLanguage(lang) {
    if (translations[lang]) {
        currentLang = lang;
        localStorage.setItem('lhh-lang', lang);
        updatePageTranslations();

        // Update language buttons
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.classList.toggle('active', btn.dataset.lang === lang);
        });

        // Trigger custom event for app.js to re-render
        window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
    }
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
    updatePageTranslations();

    // Set up language switcher buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === currentLang);
        btn.addEventListener('click', () => setLanguage(btn.dataset.lang));
    });
});
